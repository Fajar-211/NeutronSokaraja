      <script>
    // 🔍 Live Search
    document.getElementById("simple-search").addEventListener("keyup", function () {
      const filter = this.value.toLowerCase();
      const rows = document.querySelectorAll("#tbody tr");

      rows.forEach(row => {
        const tentor = row.cells[0].textContent.toLowerCase();
        const mapel = row.cells[1].textContent.toLowerCase();
        const catatan = row.cells[2].textContent.toLowerCase();

        if (tentor.includes(filter) || mapel.includes(filter) || catatan.includes(filter)) {
          row.style.display = "";
        } else {
          row.style.display = "none";
        }
      });
    });
  </script>
